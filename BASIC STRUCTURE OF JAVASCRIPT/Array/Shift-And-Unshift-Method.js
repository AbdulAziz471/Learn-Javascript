// .unshift() and .shift():

// .unshift(element): Adds an element to the beginning of an array.
// .shift(): Removes the first element from the beginning of an array.

let fruits = ["apple", "banana", "orange"];

fruits.unshift("kiwi"); // ["kiwi", "apple", "banana", "orange"]
fruits.shift();         // ["apple", "banana", "orange"]
